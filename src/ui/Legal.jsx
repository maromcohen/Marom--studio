export default function Legal({ doc, onClose }) {
  const ref = useRef()

  useEffect(() => {
    if (!doc) return
    const el = ref.current
    el.querySelector('.legal-close')?.focus()
    const onKey = (e) => {
      if (e.key === 'Escape') { onClose(); return }
      if (e.key !== 'Tab') return
      const items = [...el.querySelectorAll('a[href], button')]
      if (!items.length) return
      const first = items[0], last = items[items.length - 1]
      if (e.shiftKey && document.activeElement === first) { e.preventDefault(); last.focus() }
      else if (!e.shiftKey && document.activeElement === last) { e.preventDefault(); first.focus() }
    }
    document.addEventListener('keydown', onKey)
    return () => document.removeEventListener('keydown', onKey)
  }, [doc, onClose])

  if (!doc) return null
  const d = DOCS[doc]

  return (
    <div className="legal" role="dialog" aria-modal="true" aria-label={d.title} ref={ref}>
      <div className="legal-bg" onClick={onClose} />
      <div className="legal-card">
        <button className="legal-close" onClick={onClose} aria-label="סגירה">✕</button>
        <h3>{d.title}</h3>
        <div className="legal-body">{d.body}</div>
      </div>
    </div>
  )
}
